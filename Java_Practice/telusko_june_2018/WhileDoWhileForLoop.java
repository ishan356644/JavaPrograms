package telusko_june_2018;

public class WhileDoWhileForLoop {

	public static void main(String[] args) {
		
		int x=1;
		while(x<10) {
			System.out.println(x);
			x++;
		}
System.out.println("**********************");
		int y=10;
		
		do {
			
			System.out.println(y);
		}while(y<10);
	
	System.out.println("**********************");
	
	for(int z=1;z<10;z++) {
		System.out.println(z);
	}
	
	}
}
